@app.route('/upload', methods=['POST'])
def upload_files():
    if 'files[]' not in request.files:
        return jsonify({'error': 'No response sheets uploaded'}), 400

    shift = request.form.get('shift', '17s1')
    active_answer_key = ANSWER_KEYS.get(shift, ANSWER_KEYS["17s1"])

    files = request.files.getlist('files[]')
    all_parsed_data = {}

    for file in files:
        if file and file.filename.lower().endswith('.pdf'):
            try:
                with pdfplumber.open(file) as pdf:
                    full_text = ""
                    for page in pdf.pages:
                        text = page.extract_text()
                        if text:
                            full_text += text + "\n"
                
                # Ultra-forgiving Question ID finder
                id_matches = list(re.finditer(r'(?:Question\s*ID|Q\s*\.?\s*ID)\s*[:\-]?\s*(\d{4,15})', full_text, re.IGNORECASE))
                
                for i, match in enumerate(id_matches):
                    q_id = match.group(1).strip()
                    start_pos = match.end()
                    end_pos = id_matches[i+1].start() if i + 1 < len(id_matches) else len(full_text)
                    block = full_text[start_pos:end_pos]

                    # Ultra-forgiving Answer finder (handles missing words, weird spacing)
                    ans_pattern = r'(?:Answer\s*Given\s*By\s*Candidate|Chosen\s*Option|Candidate\s*Answer)(?:\s*is)?\s*[:\-]?\s*([1-4])'
                    ans_match = re.search(ans_pattern, block, re.IGNORECASE)
                    
                    if ans_match:
                        ans = ans_match.group(1).strip()
                    elif re.search(r'(NOT\s*ANSWERED|Not\s*Attempted|Unanswered|--)', block, re.IGNORECASE):
                        ans = "Not Attempted"
                    else:
                        ans = "Not Attempted"

                    all_parsed_data[q_id] = ans
            except Exception as e:
                print(f"Error parsing file {file.filename}: {e}")

    results = []
    score, correct, incorrect, unattempted = 0, 0, 0, 0

    for q_id, cand_ans in all_parsed_data.items():
        official_ans = active_answer_key.get(q_id, "Unknown")
        if official_ans == "Unknown":
            continue

        if official_ans.lower() == "dropped":
            score += 4
            status = "Dropped (+4)"
        elif cand_ans == "Not Attempted":
            unattempted += 1
            status = "Unattempted (0)"
        elif cand_ans in official_ans.split(','):
            score += 4
            correct += 1
            status = "Correct (+4)"
        else:
            score -= 1
            incorrect += 1
            status = "Incorrect (-1)"

        results.append({
            "question_id": q_id,
            "official_answer": official_ans,
            "candidate_answer": cand_ans,
            "status": status
        })

    return jsonify({
        "total_score": score,
        "correct": correct,
        "incorrect": incorrect,
        "unattempted": unattempted,
        "questions": results
    })

